require "test_helper"
require "pn532/frame"

class FrameBuildTest < Minitest::Test
  # GetFirmwareVersion: cmd=0x02, data=[]
  # 期待: 00 00 FF 02 FE D4 02 2A 00
  #   LEN=2 (TFI+cmd), LCS=0x100-0x02=0xFE
  #   DCS=0x100-(0xD4+0x02)=0x2A
  def test_build_get_firmware_version
    bytes = PN532::Frame.build(0x02, [])
    assert_equal [0x00, 0x00, 0xFF, 0x02, 0xFE, 0xD4, 0x02, 0x2A, 0x00], bytes
  end

  # InListPassiveTarget Type A: cmd=0x4A, data=[0x01, 0x00]
  # LEN = 1 + 1 + 2 = 4, LCS = 0xFC
  # DCS = 0x100 - (0xD4 + 0x4A + 0x01 + 0x00) = 0x100 - 0x11F = 0xE1
  def test_build_inlist_passive_target_typeA
    bytes = PN532::Frame.build(0x4A, [0x01, 0x00])
    assert_equal [0x00, 0x00, 0xFF, 0x04, 0xFC, 0xD4, 0x4A, 0x01, 0x00, 0xE1, 0x00], bytes
  end

  # InListPassiveTarget FeliCa: cmd=0x4A, data=[0x01, 0x01, 0x00, 0xFF, 0xFF, 0x01, 0x00]
  # LEN = 1 + 1 + 7 = 9, LCS = 0xF7
  # sum = 0xD4 + 0x4A + 0x01 + 0x01 + 0x00 + 0xFF + 0xFF + 0x01 + 0x00 = 0x31F
  # DCS = 0x100 - (0x31F & 0xFF) = 0x100 - 0x1F = 0xE1
  def test_build_inlist_passive_target_felica
    bytes = PN532::Frame.build(0x4A, [0x01, 0x01, 0x00, 0xFF, 0xFF, 0x01, 0x00])
    assert_equal [0x00, 0x00, 0xFF, 0x09, 0xF7, 0xD4, 0x4A, 0x01, 0x01, 0x00, 0xFF, 0xFF, 0x01, 0x00, 0xE1, 0x00], bytes
  end
end
