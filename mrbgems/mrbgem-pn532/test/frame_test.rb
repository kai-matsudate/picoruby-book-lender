require "test_helper"
require "pn532/frame"

class FrameBuildTest < Minitest::Test
  # GetFirmwareVersion: cmd=0x02, data=[]
  # 期待: 00 00 FF 02 FE D4 02 2A 00
  #   LEN=2 (TFI+cmd), LCS=0x100-0x02=0xFE
  #   DCS=0x100-(0xD4+0x02)=0x2A
  def test_build_get_firmware_version
    bytes = PN532::Frame.build(0x02, [])
    assert_equal [0x00, 0x00, 0xFF, 0x02, 0xFE, 0xD4, 0x02, 0x2A, 0x00], bytes
  end

  # InListPassiveTarget Type A: cmd=0x4A, data=[0x01, 0x00]
  # LEN = 1 + 1 + 2 = 4, LCS = 0xFC
  # DCS = 0x100 - (0xD4 + 0x4A + 0x01 + 0x00) = 0x100 - 0x11F = 0xE1
  def test_build_inlist_passive_target_typeA
    bytes = PN532::Frame.build(0x4A, [0x01, 0x00])
    assert_equal [0x00, 0x00, 0xFF, 0x04, 0xFC, 0xD4, 0x4A, 0x01, 0x00, 0xE1, 0x00], bytes
  end

  # InListPassiveTarget FeliCa: cmd=0x4A, data=[0x01, 0x01, 0x00, 0xFF, 0xFF, 0x01, 0x00]
  # LEN = 1 + 1 + 7 = 9, LCS = 0xF7
  # sum = 0xD4 + 0x4A + 0x01 + 0x01 + 0x00 + 0xFF + 0xFF + 0x01 + 0x00 = 0x31F
  # DCS = 0x100 - (0x31F & 0xFF) = 0x100 - 0x1F = 0xE1
  def test_build_inlist_passive_target_felica
    bytes = PN532::Frame.build(0x4A, [0x01, 0x01, 0x00, 0xFF, 0xFF, 0x01, 0x00])
    assert_equal [0x00, 0x00, 0xFF, 0x09, 0xF7, 0xD4, 0x4A, 0x01, 0x01, 0x00, 0xFF, 0xFF, 0x01, 0x00, 0xE1, 0x00], bytes
  end
end

class FrameParseAckTest < Minitest::Test
  def test_classify_raises_on_truncated_after_start
    # 5 bytes total (passes size < 5 guard), start code at index 1,
    # but only 1 byte remains after start code → truncated
    bytes = [0x00, 0x00, 0x00, 0xFF, 0x06]
    assert_raises(PN532::ProtocolError) { PN532::Frame.classify(bytes) }
  end

  def test_recognizes_ack_frame
    bytes = [0x00, 0x00, 0xFF, 0x00, 0xFF, 0x00]
    assert_equal :ack, PN532::Frame.classify(bytes)
  end

  def test_recognizes_nack_frame
    bytes = [0x00, 0x00, 0xFF, 0xFF, 0x00, 0x00]
    assert_equal :nack, PN532::Frame.classify(bytes)
  end

  def test_recognizes_error_frame
    bytes = [0x00, 0x00, 0xFF, 0x01, 0xFF, 0x7F, 0x81, 0x00]
    assert_equal :error, PN532::Frame.classify(bytes)
  end

  def test_recognizes_information_frame
    # GetFirmwareVersion 応答
    bytes = [0x00, 0x00, 0xFF, 0x06, 0xFA, 0xD5, 0x03, 0x32, 0x01, 0x06, 0x07, 0xE8, 0x00]
    assert_equal :information, PN532::Frame.classify(bytes)
  end
end

class FrameParseResponseTest < Minitest::Test
  def test_parse_response_extracts_cmd_and_data
    # GetFirmwareVersion 応答: cmd=0x03, data=[0x32, 0x01, 0x06, 0x07]
    bytes = [0x00, 0x00, 0xFF, 0x06, 0xFA, 0xD5, 0x03, 0x32, 0x01, 0x06, 0x07, 0xE8, 0x00]
    cmd, data = PN532::Frame.parse_response(bytes)
    assert_equal 0x03, cmd
    assert_equal [0x32, 0x01, 0x06, 0x07], data
  end

  def test_parse_response_raises_on_bad_lcs
    # LEN=0x06 だが LCS が 0xFF (本当は 0xFA)
    bytes = [0x00, 0x00, 0xFF, 0x06, 0xFF, 0xD5, 0x03, 0x32, 0x01, 0x06, 0x07, 0xE8, 0x00]
    assert_raises(PN532::ChecksumError) { PN532::Frame.parse_response(bytes) }
  end

  def test_parse_response_raises_on_bad_dcs
    # DCS が 0x00 (本当は 0xE8)
    bytes = [0x00, 0x00, 0xFF, 0x06, 0xFA, 0xD5, 0x03, 0x32, 0x01, 0x06, 0x07, 0x00, 0x00]
    assert_raises(PN532::ChecksumError) { PN532::Frame.parse_response(bytes) }
  end

  def test_parse_response_raises_on_wrong_tfi
    # TFI が 0xD4 (host→PN532) になっているおかしなフレーム
    # LEN=2, LCS=0xFE, sum(D4+03)=D7, DCS=29
    bytes = [0x00, 0x00, 0xFF, 0x02, 0xFE, 0xD4, 0x03, 0x29, 0x00]
    assert_raises(PN532::ProtocolError) { PN532::Frame.parse_response(bytes) }
  end

  def test_parse_response_raises_on_truncated_payload
    # LCS は整合 (LEN=0x06, LCS=0xFA, sum=0x100)、しかしペイロードが不足
    bytes = [0x00, 0x00, 0xFF, 0x06, 0xFA, 0xD5, 0x03, 0x32]
    assert_raises(PN532::ProtocolError) { PN532::Frame.parse_response(bytes) }
  end
end
